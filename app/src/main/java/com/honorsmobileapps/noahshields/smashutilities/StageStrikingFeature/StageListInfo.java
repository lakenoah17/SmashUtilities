package com.honorsmobileapps.noahshields.smashutilities.StageStrikingFeature;

public class StageListInfo {
    private String name;
    private String key;
    public StageListInfo(String name, String key){
        this.name = name;
        this.key = key;
    }
    public String getName() {
        return name;
    }
    public String getKey() {
        return key;
    }
}
